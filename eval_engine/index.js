const { GoogleGenerativeAI } = require("@google/generative-ai");
const fs = require("fs");
require("dotenv").config();

// Initialize the Google Gen AI SDK
const genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);

/**
 * Helper function to convert a local file into the format Gemini expects
 */
function fileToGenerativePart(filePath, mimeType) {
  return {
    inlineData: {
      data: Buffer.from(fs.readFileSync(filePath)).toString("base64"),
      mimeType
    },
  };
}

/**
 * Compares a student UI screenshot against a reference UI screenshot
 * @param {string} studentImagePath - Path to the screenshot generated by Puppeteer
 * @param {string} referenceImagePath - Path to the baseline image uploaded by the trainer
 * @returns {Promise<Object>} JSON object containing the evaluation feedback
 */
async function generateFeedback(studentImagePath, referenceImagePath) {
  try {
    // gemini-1.5-flash is ideal for fast, multimodal tasks
    const model = genAI.getGenerativeModel({
      model: "gemini-2.5-flash",
      generationConfig: {
        // Enforce JSON output natively
        responseMimeType: "application/json", 
      }
    });

    const studentImagePart = fileToGenerativePart(studentImagePath, "image/png");
    const referenceImagePart = fileToGenerativePart(referenceImagePath, "image/png");

    const prompt = `You are an expert frontend web development evaluator.
    Compare the two provided UI screenshots. 
    Image 1: The Reference UI (baseline expected result).
    Image 2: The Student UI (actual HTML/CSS/JS implementation).

    Evaluate the student's implementation based on the visual output. 
    Carefully identify:
    - Layout differences
    - Alignment issues
    - Color mismatches
    - Missing or extra UI elements

    Return the output strictly in the following JSON format:
    {
      "correct_elements": ["List of what the student got right"],
      "incorrect_elements": ["List of what is wrong or mismatched"],
      "improvement_suggestions": ["Actionable advice for the student to fix the HTML/CSS issues"]
    }`;

    // Send the prompt and both images to the model
    const result = await model.generateContent([
      prompt,
      referenceImagePart, 
      studentImagePart
    ]);

    const responseText = result.response.text();
    
    // Parse the JSON text returned by the model
    return JSON.parse(responseText);

  } catch (error) {
    console.error("PRISM Feedback Error:", error.message);
    throw error;
  }
}

module.exports = { generateFeedback };